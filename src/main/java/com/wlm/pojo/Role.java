package com.wlm.pojo;

public class Role {
    private Integer roleid;

    private Integer role;

    private Integer aminRoleId;

    public Integer getRoleid() {
        return roleid;
    }

    public void setRoleid(Integer roleid) {
        this.roleid = roleid;
    }

    public Integer getRole() {
        return role;
    }

    public void setRole(Integer role) {
        this.role = role;
    }

    public Integer getAminRoleId() {
        return aminRoleId;
    }

    public void setAminRoleId(Integer aminRoleId) {
        this.aminRoleId = aminRoleId;
    }
}